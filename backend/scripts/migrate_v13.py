def main():
    path = Path(__file__).parent.parent / "data" / "stocks.json"
    data = json.load(open(path, encoding="utf-8"))
    existing = {s["symbol"]: s for s in data}

    merged = []
    seen = set()
    for row in SPEC:
        sym, name, layer_id, sub, tier, note, sec = row
        _disp, layer_name, theme = LAYER_META[layer_id]
        stock = {
            "symbol": sym,
            "name": name,
            "layer": layer_id,
            "layer_name": layer_name,
            "sub_category": sub,
            "note": note,
            "theme": theme,
            "tier": tier,
            "enabled": True,
        }
        if sec:
            stock["secondary_layers"] = sec
            stock["themes"] = sorted({theme, *[LAYER_META[l][2] for l in sec]})
        # Preserve existing optional fields (logo_id, industry_role) if the
        # stock was already in the DB
        prev = existing.get(sym)
        if prev:
            if prev.get("logo_id"):
                stock["logo_id"] = prev["logo_id"]
            if prev.get("industry_role"):
                stock["industry_role"] = prev["industry_role"]
        merged.append(stock)
        seen.add(sym)

    # Preserve user-added stocks that aren't in the spec as tier=3
    for sym, s in existing.items():
        if sym in seen:
            continue
        s2 = dict(s)
        s2.setdefault("tier", 3)
        s2.setdefault("enabled", True)
        merged.append(s2)

    with open(path, "w", encoding="utf-8") as f:
        json.dump(merged, f, ensure_ascii=False, indent=2)

    by_tier = {1: 0, 2: 0, 3: 0}
    for s in merged:
        by_tier[s["tier"]] = by_tier.get(s["tier"], 0) + 1
    by_theme = {}
    for s in merged:
        by_theme[s["theme"]] = by_theme.get(s["theme"], 0) + 1
    print(f"Total: {len(merged)}")
    print(f"By tier: {by_tier}")
    print(f"By theme: {by_theme}")


if __name__ == "__main__":
    main()
